#if there are VM with same username 
servers=(IP1 IP2)
uname="root"
sshkey=~/.ssh/id_rsa

for server in ${servers[@]};do
    ssh -o StrictHostKeyChecking=no -i $sshkey $uname@$server 'mkdir -p llll' && echo "Successfully logged in to $server and created directory llll"
done

