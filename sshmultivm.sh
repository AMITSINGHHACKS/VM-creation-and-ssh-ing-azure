#if there are VM with multiple username 1 v 1 login
servers=(IP1 IP2)
uname=(uname1 uname2)
sshkey=~/.ssh/id_rsa

for ((i=0; i<${#servers[@]}; i++)); do
    ssh -o StrictHostKeyChecking=no -i $sshkey ${uname[$i]}@${servers[$i]} 'mkdir -p llll' && echo "Successfully logged in to ${servers[$i]} and created directory llll"
done

